int
sys_chdir(void)
{
  char *path;
  struct inode *ip;
  struct proc *curproc = myproc();
  
  begin_op();
  if(argstr(0, &path) < 0 || (ip = namei(path)) == 0){
    end_op();
    return -1;
  }
  ilock(ip);
  if(ip->type != T_DIR){
    iunlockput(ip);
    end_op();
    return -1;
  }
  iunlock(ip);
  iput(curproc->cwd);
  end_op();
  curproc->cwd = ip;
  return 0;
}

int
sys_exec(void)
{
  char *path, *argv[MAXARG];
  int i;
  uint uargv, uarg;

  if(argstr(0, &path) < 0 || argint(1, (int*)&uargv) < 0){
    return -1;
  }
  memset(argv, 0, sizeof(argv));
  for(i=0;; i++){
    if(i >= NELEM(argv))
      return -1;
    if(fetchint(uargv+4*i, (int*)&uarg) < 0)
      return -1;
    if(uarg == 0){
      argv[i] = 0;
      break;
    }
    if(fetchstr(uarg, &argv[i]) < 0)
      return -1;
  }
  return exec(path, argv);
}

int
sys_pipe(void)
{
  int *fd;
  struct file *rf, *wf;
  int fd0, fd1;

  if(argptr(0, (void*)&fd, 2*sizeof(fd[0])) < 0)
    return -1;
  if(pipealloc(&rf, &wf) < 0)
    return -1;
  fd0 = -1;
  if((fd0 = fdalloc(rf)) < 0 || (fd1 = fdalloc(wf)) < 0){
    if(fd0 >= 0)
      myproc()->ofile[fd0] = 0;
    fileclose(rf);
    fileclose(wf);
    return -1;
  }
  fd[0] = fd0;
  fd[1] = fd1;
  return 0;
}

int
sys_lseek(void)
{
  int fd, offset;
  int extra_bytes;
  char null_block[KSTACKSIZE];
  struct file *f;
  struct proc *curproc = myproc();

  if(argint(0, &fd) < 0) // Get the integer that represents the file descriptor.
    return -1;
  if(argint(1, &offset) < 0) // Get the integer that represents the offset.
    return -1;
  if(argfd(fd, 0, &f) < 0) // Convert the integer to the file descriptor.
    return -1;
  if(f->type == FD_PIPE) // Make sure we're not dealing with a pipe.
    return -1;
	
  
  // We need to check if lseek sets off beyond f->ip->size
  if(offset > f->ip->size) {
    extra_bytes = offset - f->ip->size;

    // Create a null block. Basically fill a block of size KSTACKSIZE with '\0'
    // We are creating this null block inside so that we only do it if the condition is met so we avoid unnecessary empty block initilization.
    for (int i=0;i<KSTACKSIZE;i++) {
      null_block[i]='\0';
    }
    // Now we need filewrite to fill holes in the file with zero
    while(extra_bytes > 0) {
      // Since we are filling holes with zeros simply pass the null block we just created to filewrite
      filewrite(f, (char *)null_block, extra_bytes % (KSTACKSIZE + 1));
      // Update the remaining extra bytes now that we wrote a block of zeros
      extra_bytes -= KSTACKSIZE;
    }
  
  }
  
  // Update f->off
  f->off = offset;
  return f->off;
}

